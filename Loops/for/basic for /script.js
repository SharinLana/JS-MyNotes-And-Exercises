//АРИФМЕТИЧЕСКИЕ ОПЕРАЦИИ С ЧИСЛАМИ В ЦИКЛЕ

//Найдем сумму целых чисел от 1 до 100
let result = 0;
for (let i3 = 1; i3 <= 100; i3++) {
	result = result + i3; //вариант: result += i3
}
console.log(result); // 5050

//Как это работает: изначально переменная result имеет значение 0, 
//затем при первом проходе цикла в нее записывается ее текущее содержимое (это 0) 
//плюс значение счетчика (это 1).
//Получится, что в result запишется 0 + 1 и теперь там будет лежать число 1.
//При следующем проходе цикла в result запишется текущее значение result (это 1)
//плюс текущее значение счетчика (это 2), то есть 1 + 2 = 3.
//И так далее, пока цикл не закончится

