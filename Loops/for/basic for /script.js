/* Loop FOR in JS */

//Syntax of the FOR loop:
/*
for ( начальные команды; условие окончания; команды после прохода ) {
	тело цикла
}

Начальные команды - это то, что выполнится перед стартом цикла. Они выполнятся только один раз. 
Обычно там размещают начальные значения счетчиков, например: i = 0.

Условие окончания цикла - это условие, при котором цикл будет крутится, 
пока оно истинное, например: i <= 10.

Команды после прохода цикла - это команды, которые будут выполнятся каждый раз при окончании прохода цикла. 
Обычно там увеличивают/уменьшают счетчики, например: i++/i--.
*/

for (let i = 1; i <= 9; i++) {
    console.log(i);
}

for (let j = 1; j <= 100; j++) {
    console.log(j);
}

for (let k = 11; k <= 33; k++) {
    console.log(k);
}

for (let k2 = 100; k2 >= 0; k2--) {
    console.log(k2);
}

//Вывести четные числа в промежутке от 0 до 100
