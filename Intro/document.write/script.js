/* Document.write */

//Иногда требуется вывести какой-нибудь текст в окно браузера. 
//Это делается с помощью команды document.write.

document.write(123); //числовое значение

document.write('<br> text'); //string as a new paragraph

document.write('<br> <b>text</b>'); // text in bold as a new paragraph

document.write('<br> <em>text</em>'); //text in cursive as a new paragraph

let str = '<br> text';
document.write(str); 



