/* Negative Numbers */

let a = -1;
alert(a); // выведет -1

//Знак минус можно писать как к числам, так и к переменным:
let b = 1;
let c = -b; 
alert(c);   // -1

let d = 1;
alert(-d); // -1

let e = -100;
console.log(e);

let f = 18;
console.log(-f);


/* Positive sign in front of variables */
//Перед положительными числами можно ставить знак "плюс".
//Фактически этот плюс ничего не делает, но вполне является допустимым
let g = +1;
alert(g); // выведет 1